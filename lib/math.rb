def addition(num1, num2)
sum = num1 + num2
return sum
end

def subtraction(num1, num2)
sum = num1 - num2
return sum
end

def division(num1, num2)
sum = num1 / num2
return sum
end

def multiplication(num1, num2)
sum = num1 * num2
return sum
end

def modulo(num1, num2)
r.0 = num1.0 / num2.0
return r.0
end

def square_root(num)
sum = Math.sqrt(num)
return sum
end
